(*val pr_bind : forall 'a 'b. parse_result 'a -> ('a -> parser 'b) -> parse_result 'b*)
let pr_bind x f:'b parse_result=  
 ((match x with
  | PR_success( v, pc) -> f v pc
  | PR_fail( err, pc)  -> PR_fail( err, pc)
  ))

