 (fun (pc: parse_context) -> 
      if (listEqualBy (=) pc.pc_bytes []) then 
        PR_success( None, pc)
      else
        pr_post_map1 
          (parse_list (parse_location_list_item c cuh) pc)
          (fun llis -> (Some (pc.pc_offset, llis))))

